    private void abrirArchivo() {
        ruta = "";
        try
        {
         /**llamamos el metodo que permite cargar la ventana*/
         JFileChooser file=new JFileChooser();
         file.showOpenDialog(this);
         /**abrimos el archivo seleccionado*/
         File abre = file.getSelectedFile();
         ruta = abre.getPath();
         //this.imprimirMensaje("Archivo: "+ruta);
         this.jLabel3.setText(ruta);
        }catch(Exception e){
            this.imprimirError("No se ha podido abrir el archivo indicado");
        }
  }
    public void imprimirError(String m){
        
        errores = true;
        jTextArea2.setText(jTextArea2.getText()+"\n"+m);
        
    }
    
    public void finalizarErrores(){
        if (!errores){
            jTextArea2.setForeground(Color.GREEN);
            imprimirError("Finalizado con 0 errores");
        }
    }
    
    public void imprimirMensaje(String m){
        
        jTextArea1.setText(jTextArea1.getText()+"\n"+m);
        
    }
    
    public void limpiarError(){
        errores = false;
        jTextArea2.setForeground(Color.RED);
        jTextArea2.setText("");
    }
    
    public void limpiarMensajes(){
        jTextArea1.setText("");
    }
    
